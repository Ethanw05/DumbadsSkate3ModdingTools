    /// <summary>
    /// Resolve a source's channels and report each one's full GUID, encoded
    /// payload size, and lightmap flag — WITHOUT writing any PSG. Drives the
    /// Stage-2 budget plan. The full encode it triggers is cached, so the
    /// subsequent <see cref="EmitPlanned"/> pass reuses it for free.
    /// </summary>
    public static IReadOnlyList<(string ChannelName, ulong FullGuid, long PayloadSize, bool IsLightmap)> MeasureChannels(
        ResolvedGlbTextureSources sources,
        TextureDeduplicationRegistry deduplicationRegistry,
        CancellationToken cancellationToken = default)
    {
        var outList = new List<(string, ulong, long, bool)>(4);
        Measure(sources.Diffuse);
        Measure(sources.Normal);
        Measure(sources.Lightmap);
        Measure(sources.Specular);
        return outList;

        void Measure(ResolvedChannelSource? channel)
        {
            if (channel == null) return;
            cancellationToken.ThrowIfCancellationRequested();
            bool isNormalChannel = string.Equals(channel.MeshChannelName, "normal", StringComparison.OrdinalIgnoreCase);
            bool preserveAlpha = channel.NeedsAlpha && !isNormalChannel;
            bool reduceBc1NormalArtifacts = isNormalChannel && !channel.SourceIsDds;
            try
            {
                var dds = deduplicationRegistry.GetOrEncode(
                    channel.EncodedImageBytes,
                    channel.SourceIsDds,
                    channel.GenerateMipMaps,
                    preserveAlpha,
                    forceOpaqueAlpha: isNormalChannel,
                    reduceBc1NormalArtifacts: reduceBc1NormalArtifacts,
                    maxDimension: null);
                // Stage 3: content-addressed GUID — derived from the encoded
                // DDS, NOT the GLB/material/channel path. Identical content
                // anywhere → one GUID → one PSG (global dedup).
                ulong fullGuid = TextureGuidStrategy.ContentAddressedFullGuid(dds);
                outList.Add((channel.MeshChannelName, fullGuid, dds.Payload.LongLength, channel.IsLightmapChannel));
            }
            catch
            {
                // Measurement failure (bad source). Fall back to the legacy
                // path-based GUID so the planner still has a stable record;
                // the emit pass will log the real encode error and also fail
                // there, so this row simply never gets emitted.
                ulong fallbackGuid = ComputeChannelFullGuid(sources, channel);
                outList.Add((channel.MeshChannelName, fallbackGuid, 0L, channel.IsLightmapChannel));
            }
        }
    }

