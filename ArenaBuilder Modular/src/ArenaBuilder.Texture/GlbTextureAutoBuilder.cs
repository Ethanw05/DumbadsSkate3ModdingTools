    private static GlbTextureAutoBuildResult EmitVariantToTile(
        ResolvedGlbTextureSources sources,
        string outputDirectory,
        TextureDeduplicationRegistry deduplicationRegistry,
        bool isSmallVariant,
        int smallDownscaleFactor,
        int smallMinDim,
        LightmapEmitMode lightmapMode,
        ChannelPlacementResolver? placement,
        CancellationToken cancellationToken)
    {
        if (sources is null) throw new ArgumentNullException(nameof(sources));
        if (deduplicationRegistry is null) throw new ArgumentNullException(nameof(deduplicationRegistry));
        if (string.IsNullOrWhiteSpace(outputDirectory))
            throw new ArgumentException("Output directory is required.", nameof(outputDirectory));

        Directory.CreateDirectory(outputDirectory);

        var built = new List<BuiltTexturePsg>();
        var warnings = new List<string>(sources.ResolveWarnings);

        ulong? diffuseGuid = EmitChannel(sources.Diffuse);
        ulong? normalGuid = EmitChannel(sources.Normal);
        ulong? lightmapGuid = EmitChannel(sources.Lightmap);
        ulong? specularGuid = EmitChannel(sources.Specular);

        return new GlbTextureAutoBuildResult(
            diffuseGuid,
            normalGuid,
            lightmapGuid,
            specularGuid,
            built,
            warnings,
            sources.AttributorMaterialStream,
            sources.ChannelConfig);

        // Returns the FULL-variant GUID for every channel (bit 62 clear) — that is what mesh
        // channels reference regardless of which variant we emitted into this tile. The engine
        // performs the small-variant retry by ORing bit 62 if the full lookup misses.
        ulong? EmitChannel(ResolvedChannelSource? channel)
        {
            if (channel == null) return null;
            cancellationToken.ThrowIfCancellationRequested();

            bool isNormalChannel = string.Equals(channel.MeshChannelName, "normal", StringComparison.OrdinalIgnoreCase);
            bool preserveAlpha = channel.NeedsAlpha && !isNormalChannel;
            bool reduceBc1NormalArtifacts = isNormalChannel && !channel.SourceIsDds;

            // SINGLE encode for this channel — full resolution, full mip
            // chain — shared across every tile via the encode-once cache.
            // Must run BEFORE the GUID/placement decision because Stage 3's
            // GUID is content-addressed (derived from the encoded DDS), so
            // there is nothing to key the plan on until the bytes exist.
            // The small (cPres) variant is a byte-slice of this chain, never
            // a second encode (port of BlenRose's _dds_pick_small_variant).
            DdsTextureInput fullDds;
            try
            {
                fullDds = deduplicationRegistry.GetOrEncode(
                    channel.EncodedImageBytes,
                    channel.SourceIsDds,
                    channel.GenerateMipMaps,
                    preserveAlpha,
                    forceOpaqueAlpha: isNormalChannel,
                    reduceBc1NormalArtifacts: reduceBc1NormalArtifacts,
                    maxDimension: null);
            }
            catch (Exception ex)
            {
                warnings.Add($"Encode failed for {sources.GlbPath} channel '{channel.MeshChannelName}': {ex.Message}");
                return null;
            }

            // Stage 3: content-addressed GUID — function of the encoded DDS
            // only (port of BlenRose _tex_guid_from_dedupe_key). Identical
            // content anywhere collapses to one GUID / one PSG.
            ulong fullGuid = TextureGuidStrategy.ContentAddressedFullGuid(fullDds);

            bool channelEmitsAsSmall = isSmallVariant;

            // Stage-2 budget plan resolver (when supplied) is authoritative:
            // decides Skip / Small / Full for THIS channel in THIS tile,
            // keyed on the content-addressed GUID computed above.
            if (placement != null)
            {
                ChannelEmitDecision decision = placement(fullGuid, channel.IsLightmapChannel);
                switch (decision)
                {
                    case ChannelEmitDecision.Skip:
                        // Don't write a PSG into THIS tile — but still report
                        // the full GUID so the mesh material binds it. The
                        // bytes live in cPres_Global (promoted) or a cTex tile
                        // (streamed); the engine's full-GUID lookup (with the
                        // bit-62 cPres fallback retry) resolves it at runtime.
                        return fullGuid;
                    case ChannelEmitDecision.Small:
                        channelEmitsAsSmall = true;
                        break;
                    case ChannelEmitDecision.Full:
                        channelEmitsAsSmall = false;
                        break;
                }
            }
            else if (channel.IsLightmapChannel)
            {
                // Legacy (GlobalOnly / no-plan) path: lightmap routing
                // overrides the dual-tier policy. Skate's engine pulls
                // lightmap PSGs out of the resident per-tile cPres
                // collection, never the streamed cTex one — a cTex emit
                // drops the lightmap; a per-tile cPres (small) emit promotes
                // it to full-res / full-GUID so the mesh ref's primary
                // lookup resolves straight out of that cPres tile.
                switch (lightmapMode)
                {
                    case LightmapEmitMode.SkipLightmap:
                        return null;
                    case LightmapEmitMode.CPresFullResLightmap:
                        channelEmitsAsSmall = false;
                        break;
                    case LightmapEmitMode.Default:
                    default:
                        break;
                }
            }

            DdsTextureInput ddsInput = fullDds;
            bool wroteSmall = false;
            if (channelEmitsAsSmall)
            {
                if (DdsMipSlicer.TrySliceSmallVariant(
                        fullDds, smallDownscaleFactor, smallMinDim, out DdsTextureInput? small)
                    && small != null)
                {
                    ddsInput = small;
                    wroteSmall = true;
                }
                else
                {
                    // No usable deeper mip (single-mip source, or already
                    // small enough that the slice level clamps to 0).
                    // BlenRose's `_resolve_small → None`: emit the FULL
                    // texture into the cPres tile under the FULL GUID. The
                    // mesh ref's primary lookup resolves it straight out of
                    // this resident tile; no broken tiny re-encode.
                    ddsInput = fullDds;
                    wroteSmall = false;
                }
            }

            ulong writeGuid = wroteSmall
                ? TextureGuidStrategy.MakeSmallVariantGuid(fullGuid)
                : fullGuid;

            var psg = deduplicationRegistry.WriteLogicalGuidPsg(
                ddsInput,
                writeGuid,
                channel.MeshChannelName,
                channel.ImageName,
                outputDirectory);
            built.Add(psg);
            return fullGuid;
        }
    }

    /// <summary>
    /// Phase 2 of the texture build: write up to four PSG files into <paramref name="outputDirectory"/>
    /// from a previously-<see cref="ResolveSourcesFromGlb">resolved</see> GLB. When a registry is
    /// supplied, BCn encoding runs at most once per (sourceBytes,flags) tuple per build (see
    /// <see cref="TextureDeduplicationRegistry.GetOrEncode"/>); identical PSGs within the same
    /// <paramref name="deduplicationScope"/> are deduplicated to a single GUID/file.
    ///
    /// Call this once per cPres tile that should hold a copy of the GLB's textures: pass a unique
    /// <paramref name="deduplicationScope"/> per tile (e.g. <c>"cpres|U|V"</c>) so that byte-identical
    /// content written into different tiles still gets distinct GUIDs — required by the new
    /// per-tile self-contained collection layout.
    /// </summary>
    public static GlbTextureAutoBuildResult EmitToTile(
        ResolvedGlbTextureSources sources,
        string outputDirectory,
        TextureDeduplicationRegistry? deduplicationRegistry = null,
        string? deduplicationScope = null,
        CancellationToken cancellationToken = default)
    {
        if (sources is null)
            throw new ArgumentNullException(nameof(sources));
        if (string.IsNullOrWhiteSpace(outputDirectory))
            throw new ArgumentException("Output directory is required.", nameof(outputDirectory));

        Directory.CreateDirectory(outputDirectory);

        var built = new List<BuiltTexturePsg>();
        var warnings = new List<string>(sources.ResolveWarnings);

        ulong? diffuseGuid = EmitChannel(sources.Diffuse);
        ulong? normalGuid = EmitChannel(sources.Normal);
        ulong? lightmapGuid = EmitChannel(sources.Lightmap);
        ulong? specularGuid = EmitChannel(sources.Specular);

        return new GlbTextureAutoBuildResult(
            diffuseGuid,
            normalGuid,
            lightmapGuid,
            specularGuid,
            built,
            warnings,
            sources.AttributorMaterialStream,
            sources.ChannelConfig);

        ulong? EmitChannel(ResolvedChannelSource? channel)
        {
            if (channel == null) return null;
            cancellationToken.ThrowIfCancellationRequested();
            var psg = BuildTextureFromEncodedSource(
                channel.EncodedImageBytes,
                channel.SourceIsDds,
                channel.ImageName,
                sources.GlbIdentity,
                sources.MaterialName,
                channel.MeshChannelName,
                outputDirectory,
                channel.GenerateMipMaps,
                deduplicationRegistry,
                channel.NeedsAlpha,
                channel.IsLightmapChannel,
                deduplicationScope);
            built.Add(psg);
            return psg.Guid;
        }
    }

    private static BuiltTexturePsg? TryBuildChannelTexture(
        Material material,
        string glbPath,
        string glbIdentity,
        string materialName,
        BlenroseMaterialJsonReader.MaterialTextureConfig? jsonMaterial,
        string jsonChannelName,
        string[] glbChannelNames,
        string meshChannelName,
        string outputDirectory,
        bool generateMipMaps,
        TextureDeduplicationRegistry? deduplicationRegistry,
        string? deduplicationScope,
        out ResolvedTextureSource? resolvedSource,
        out string? warning)
    {
        warning = null;
        resolvedSource = null;

        bool isLightmapChannel = string.Equals(meshChannelName, "lightmap", StringComparison.OrdinalIgnoreCase);
        bool needsAlpha = ChannelNeedsAlpha(meshChannelName, jsonMaterial);

        // Force single-mip export only for lightmaps.
        bool wantsMipMaps = generateMipMaps && !isLightmapChannel;

        resolvedSource = TryResolveChannelSource(material, jsonMaterial, jsonChannelName, glbChannelNames, meshChannelName, out warning);
        if (resolvedSource == null)
            return null;

        return BuildTextureFromEncodedSource(
            resolvedSource.EncodedImageBytes,
            resolvedSource.SourceIsDds,
            resolvedSource.ImageName,
            glbIdentity,
            materialName,
            meshChannelName,
            outputDirectory,
            wantsMipMaps,
            deduplicationRegistry,
            needsAlpha,
            isLightmapChannel,
            deduplicationScope);
    }

    private static BuiltTexturePsg? TryBuildChannelTextureFromGlb(
        Material material,
        string glbIdentity,
        string materialName,
        string glbChannelName,
        string meshChannelName,
        string outputDirectory,
        bool generateMipMaps,
        TextureDeduplicationRegistry? deduplicationRegistry,
        bool needsAlpha,
        bool isLightmapChannel,
        out string? warning,
        string? deduplicationScope = null)
    {
        warning = null;
        try
        {
            var texture = material.FindChannel(glbChannelName)?.Texture;
            if (texture == null) return null;

            var image = texture.PrimaryImage ?? texture.FallbackImage;
            if (image == null) return null;

            MemoryImage content = image.Content;
            byte[] encodedImageBytes = ReadAllBytes(content);
            bool sourceIsDds = content.IsDds;
            string imageName = ResolveImageName(image, content, meshChannelName);

            return BuildTextureFromEncodedSource(
                encodedImageBytes,
                sourceIsDds,
                imageName,
                glbIdentity,
                materialName,
                meshChannelName,
                outputDirectory,
                generateMipMaps,
                deduplicationRegistry,
                needsAlpha,
                isLightmapChannel,
                deduplicationScope);
        }
        catch (Exception ex)
        {
            warning = $"Texture build skipped for GLB channel '{meshChannelName}': {ex.Message}";
            return null;
        }
    }

    private static BuiltTexturePsg BuildTextureFromEncodedSource(
        byte[] encodedImageBytes,
        bool sourceIsDds,
        string imageName,
        string glbIdentity,
        string materialName,
        string meshChannelName,
        string outputDirectory,
        bool generateMipMaps,
        TextureDeduplicationRegistry? deduplicationRegistry,
        bool needsAlpha,
        bool isLightmapChannel,
        string? deduplicationScope = null)
    {
        // For DDS sources we trust the author's chosen format and mip layout.
        // For raster sources we choose DXT1 vs DXT5 and honor caller mip behavior.
        //
        // Shipped PS3 normal textures are plain opaque BC1/DXT1 in the samples we inspected.
        // Treat raster normal maps as opaque so incidental PNG alpha does not turn BC1 blocks
        // into transparent-black artifacts.
        bool isNormalChannel =
            string.Equals(meshChannelName, "normal", StringComparison.OrdinalIgnoreCase);
        bool preserveAlpha = needsAlpha && !isNormalChannel;
        bool reduceBc1NormalArtifacts = isNormalChannel && !sourceIsDds;

        // Encode-once: when the registry is provided, identical (sourceBytes + flags) reuse the
        // parsed DDS that was encoded the first time we saw this content in this build, even
        // across different per-tile dedup scopes (which all produce distinct GUIDs/PSGs but share
        // one DDS payload).
        DdsTextureInput ddsInput;
        if (deduplicationRegistry != null)
        {
            ddsInput = deduplicationRegistry.GetOrEncode(
                encodedImageBytes,
                sourceIsDds,
                generateMipMaps,
                preserveAlpha,
                forceOpaqueAlpha: isNormalChannel,
                reduceBc1NormalArtifacts: reduceBc1NormalArtifacts);

            return deduplicationRegistry.RegisterOrReuse(
                ddsInput,
                meshChannelName,
                imageName,
                outputDirectory,
                deduplicationScope);
        }

        // No registry: encode and write inline (callers without a per-build cache use this path,
        // mostly the standalone CLI).
        byte[] ddsBytes = sourceIsDds
            ? encodedImageBytes
            : ImageToDdsConverter.ConvertToDds(
                encodedImageBytes,
                generateMipMaps,
                preserveAlpha,
                forceOpaqueAlpha: isNormalChannel,
                bc1NormalMapArtifactReduction: reduceBc1NormalArtifacts);
        ddsInput = DdsReader.Read(ddsBytes);

        // Without a registry there is no cross-call dedup. Fold the scope into the texture key so
        // that the same logical GLB texture, when emitted to two different output dirs, still gets
        // distinct GUIDs (each dir is a self-contained cPres tile in the new pipeline).
        string scopedIdentity = string.IsNullOrEmpty(deduplicationScope)
            ? glbIdentity
            : $"{deduplicationScope}|{glbIdentity}";
        string textureKey = TextureGuidStrategy.BuildTextureKey(scopedIdentity, materialName, meshChannelName, imageName);
        ulong textureGuid = TextureGuidStrategy.KeyToGuid(textureKey);
        string outPath = Path.Combine(outputDirectory, $"{textureGuid:X16}.psg");

        var spec = TexturePsgComposer.Compose(ddsInput, textureGuid);
        object writeLock = OutputWriteLocks.GetOrAdd(outPath, _ => new object());
        lock (writeLock)
        {
            using var fs = File.Create(outPath);
            GenericArenaWriter.Write(spec, fs);
        }

        return new BuiltTexturePsg(meshChannelName, imageName, ddsInput.Width, ddsInput.Height, textureGuid, outPath);
    }

    private static string BuildScopedGlbIdentity(string glbPath, string glbStem, string? guidNamespace)
    {
        if (string.IsNullOrWhiteSpace(guidNamespace))
            return glbStem;

        string fullGlbPath = Path.GetFullPath(glbPath).Replace('\\', '/').ToLowerInvariant();
        string fullNamespace = Path.GetFullPath(guidNamespace).Replace('\\', '/').ToLowerInvariant().TrimEnd('/');
        string relative = Path.GetRelativePath(fullNamespace, fullGlbPath).Replace('\\', '/').ToLowerInvariant();
        if (relative.StartsWith("../", StringComparison.Ordinal))
            relative = Path.GetFileNameWithoutExtension(fullGlbPath);
        return $"{fullNamespace}/{relative}";
    }

    private static BlenroseMaterialJsonReader.MaterialTextureConfig? TryResolveJsonMaterialConfig(
        string glbPath,
        string? materialsJsonPath,
        string materialName,
        string glbStem,
        List<string> warnings)
    {
        string? resolvedJsonPath = ResolveMaterialsJsonPath(glbPath, materialsJsonPath);
        if (resolvedJsonPath == null)
            return null;

        if (!File.Exists(resolvedJsonPath))
        {
            if (!string.IsNullOrWhiteSpace(materialsJsonPath))
                warnings.Add($"Materials JSON not found: {resolvedJsonPath}. Falling back to GLB textures.");
            return null;
        }

        IReadOnlyDictionary<string, BlenroseMaterialJsonReader.MaterialTextureConfig> byName;
        try
        {
            byName = BlenroseMaterialJsonReader.Read(resolvedJsonPath);
        }
        catch (Exception ex)
        {
            warnings.Add($"Failed to read materials JSON '{resolvedJsonPath}': {ex.Message}. Falling back to GLB textures.");
            return null;
        }

        if (byName.TryGetValue(materialName, out var matched))
            return matched;
        if (byName.TryGetValue(glbStem, out matched))
            return matched;

        // If exactly one unique material exists, use it as a pragmatic fallback.
        var unique = new List<BlenroseMaterialJsonReader.MaterialTextureConfig>();
        var seen = new HashSet<string>(StringComparer.OrdinalIgnoreCase);
        foreach (var candidate in byName.Values)
        {
            if (seen.Add(candidate.MaterialName))
                unique.Add(candidate);
        }

        if (unique.Count == 1)
        {
            warnings.Add($"Materials JSON had no entry for '{materialName}'. Using sole JSON material '{unique[0].MaterialName}'.");
            return unique[0];
        }

        warnings.Add($"Materials JSON had no entry for '{materialName}'. Using GLB textures for this material.");
        return null;
    }

    private static string? ResolveMaterialsJsonPath(string glbPath, string? materialsJsonPath)
    {
        if (!string.IsNullOrWhiteSpace(materialsJsonPath))
            return Path.GetFullPath(materialsJsonPath);

        string sidecarPath = Path.ChangeExtension(Path.GetFullPath(glbPath), ".json");
        return File.Exists(sidecarPath) ? sidecarPath : null;
    }

    private static string? TryGetJsonChannelImageName(
        BlenroseMaterialJsonReader.MaterialTextureConfig? jsonMaterial,
        string channelName)
    {
        if (jsonMaterial == null)
            return null;
        if (!jsonMaterial.ChannelImageNames.TryGetValue(channelName, out string? name))
            return null;
        return string.IsNullOrWhiteSpace(name) ? null : name;
    }

    private static readonly string[] KnownGlbChannels = ["BaseColor", "Normal", "Emissive", "Occlusion", "MetallicRoughness"];

    private static ResolvedTextureSource? TryResolveChannelSource(
        Material material,
        BlenroseMaterialJsonReader.MaterialTextureConfig? jsonMaterial,
        string jsonChannelName,
        string[] glbChannelNames,
        string meshChannelName,
        out string? warning)
    {
        warning = null;

        // 1) Resolve by JSON image_name hint.
        string? imageNameHint = TryGetJsonChannelImageName(jsonMaterial, jsonChannelName);
        if (!string.IsNullOrWhiteSpace(imageNameHint))
        {
            var sourceByName = TryResolveTextureSourceByImageName(material, imageNameHint, meshChannelName, out string? byNameWarning);
            if (sourceByName != null)
                return sourceByName;
            if (byNameWarning != null)
                warning = warning == null ? byNameWarning : $"{warning} | {byNameWarning}";
        }

        // 2) Fallback to GLB channel texture.
        foreach (var glbChannelName in glbChannelNames)
        {
            var sourceFromChannel = TryResolveTextureSourceFromGlbChannel(material, glbChannelName, meshChannelName, out string? glbWarning);
            if (sourceFromChannel != null)
                return sourceFromChannel;
            if (glbWarning != null)
                warning = warning == null ? glbWarning : $"{warning} | GLB {glbChannelName}: {glbWarning}";
        }

        return null;
    }

    private static ResolvedTextureSource? TryResolveTextureSourceFromGlbChannel(
        Material material,
        string glbChannelName,
        string meshChannelName,
        out string? warning)
    {
        warning = null;
        try
        {
            var texture = material.FindChannel(glbChannelName)?.Texture;
            if (texture == null) return null;

            var image = texture.PrimaryImage ?? texture.FallbackImage;
            if (image == null) return null;

            MemoryImage content = image.Content;
            byte[] encodedImageBytes = ReadAllBytes(content);
            bool sourceIsDds = content.IsDds;
            string imageName = ResolveImageName(image, content, meshChannelName);
            return new ResolvedTextureSource(encodedImageBytes, sourceIsDds, imageName);
        }
        catch (Exception ex)
        {
            warning = $"Texture source read skipped for GLB channel '{meshChannelName}': {ex.Message}";
            return null;
        }
    }

    private static ResolvedTextureSource? TryResolveTextureSourceByImageName(
        Material material,
        string imageNameHint,
        string meshChannelName,
        out string? warning)
    {
        warning = null;
        try
        {
            string hintNormalized = NormalizeImageNameForCompare(imageNameHint);
            foreach (var channelName in KnownGlbChannels)
            {
                var texture = material.FindChannel(channelName)?.Texture;
                if (texture == null) continue;

                var image = texture.PrimaryImage ?? texture.FallbackImage;
                if (image == null) continue;

                string texName = image.Name ?? "";
                if (string.IsNullOrWhiteSpace(texName) && !string.IsNullOrWhiteSpace(image.Content.SourcePath))
                    texName = Path.GetFileNameWithoutExtension(image.Content.SourcePath);
                if (string.IsNullOrWhiteSpace(texName))
                    continue;

                string texNameNormalized = NormalizeImageNameForCompare(texName);
                if (!string.Equals(texNameNormalized, hintNormalized, StringComparison.OrdinalIgnoreCase) &&
                    !texNameNormalized.Contains(hintNormalized))
                {
                    continue;
                }

                MemoryImage content = image.Content;
                byte[] encodedImageBytes = ReadAllBytes(content);
                bool sourceIsDds = content.IsDds;
                string imageName = ResolveImageName(image, content, meshChannelName);
                return new ResolvedTextureSource(encodedImageBytes, sourceIsDds, imageName);
            }
        }
        catch (Exception ex)
        {
            warning = $"Texture by name '{imageNameHint}' failed: {ex.Message}";
        }

        return null;
    }

    private static BuiltTexturePsg? TryBuildChannelTextureFromGlbByImageName(
        Material material,
        string glbIdentity,
        string materialName,
        string imageNameHint,
        string meshChannelName,
        string outputDirectory,
        bool generateMipMaps,
        TextureDeduplicationRegistry? deduplicationRegistry,
        bool needsAlpha,
        bool isLightmapChannel,
        out string? warning)
    {
        warning = null;
        try
        {
            string hintNormalized = NormalizeImageNameForCompare(imageNameHint);
            foreach (var channelName in KnownGlbChannels)
            {
                var texture = material.FindChannel(channelName)?.Texture;
                if (texture == null) continue;

                var image = texture.PrimaryImage ?? texture.FallbackImage;
                if (image == null) continue;

                string texName = image.Name ?? "";
                if (string.IsNullOrWhiteSpace(texName) && !string.IsNullOrWhiteSpace(image.Content.SourcePath))
                    texName = Path.GetFileNameWithoutExtension(image.Content.SourcePath);
                if (string.IsNullOrWhiteSpace(texName))
                    continue;

                string texNameNormalized = NormalizeImageNameForCompare(texName);

                if (string.Equals(texNameNormalized, hintNormalized, StringComparison.OrdinalIgnoreCase) ||
                    texNameNormalized.Contains(hintNormalized))
                {
                    MemoryImage content = image.Content;
                    byte[] encodedImageBytes = ReadAllBytes(content);
                    bool sourceIsDds = content.IsDds;
                    string imageName = ResolveImageName(image, content, meshChannelName);

                    return BuildTextureFromEncodedSource(
                        encodedImageBytes,
                        sourceIsDds,
                        imageName,
                        glbIdentity,
                        materialName,
                        meshChannelName,
                        outputDirectory,
                        generateMipMaps,
                        deduplicationRegistry,
                        needsAlpha,
                        isLightmapChannel);
                }
            }
        }
        catch (Exception ex)
        {
            warning = $"Texture by name '{imageNameHint}' failed: {ex.Message}";
        }
        return null;
    }

    private static string BuildGeneratedImageName(string baseImageName, string suffix)
    {
        string seed = string.IsNullOrWhiteSpace(baseImageName) ? "diffuse" : baseImageName.Trim();
        return $"{seed}__{suffix}";
    }

    private static RenderMaterialDataBuilder.BlenroseChannelConfig? EnsureGeneratedChannelsInConfig(
        RenderMaterialDataBuilder.BlenroseChannelConfig? channelConfig,
        bool addNormal,
        bool addSpecular)
    {
        if (channelConfig == null || (!addNormal && !addSpecular))
            return channelConfig;

        var channels = new HashSet<string>(channelConfig.TextureChannelNames, StringComparer.OrdinalIgnoreCase);
        bool changed = false;
        if (addNormal)
            changed |= channels.Add("normal");
        if (addSpecular)
            changed |= channels.Add("specular");
        if (!changed)
            return channelConfig;

        string[] order = ["environment", "lightmap", "decal", "specular", "diffuse", "detail", "macrooverlay", "normal", "transparent", "noise"];
        var merged = new List<string>();
        foreach (var name in order)
        {
            if (channels.Contains(name))
                merged.Add(name);
        }

        // Preserve any unknown/custom names after canonical channels.
        foreach (var name in channelConfig.TextureChannelNames)
        {
            if (!merged.Contains(name, StringComparer.OrdinalIgnoreCase))
                merged.Add(name);
        }
        foreach (var name in channels)
        {
            if (!merged.Contains(name, StringComparer.OrdinalIgnoreCase))
                merged.Add(name);
        }

        return new RenderMaterialDataBuilder.BlenroseChannelConfig(merged, channelConfig.ScalarValues);
    }

    private static string NormalizeImageNameForCompare(string imageName)
    {
        string trimmed = imageName.Trim();
        if (string.IsNullOrWhiteSpace(trimmed))
            return string.Empty;
        return Path.GetFileNameWithoutExtension(trimmed).Trim().ToLowerInvariant();
    }

    private static bool ChannelNeedsAlpha(
        string meshChannelName,
        BlenroseMaterialJsonReader.MaterialTextureConfig? jsonMaterial)
    {
        // Transparent channel always uses alpha.
        if (string.Equals(meshChannelName, "transparent", StringComparison.OrdinalIgnoreCase))
            return true;

        if (jsonMaterial != null)
        {
            // Prefer explicit per-channel alpha flags (from BlenRose JSON when present).
            if (jsonMaterial.ChannelAlphaFlags.TryGetValue(meshChannelName, out bool hasAlpha))
                return hasAlpha;
        }

        // By default, all non-transparent channels are treated as opaque unless
        // JSON explicitly marks them as alpha-using.
        return false;
    }

    private static byte[] ReadAllBytes(MemoryImage memoryImage)
    {
        using var stream = memoryImage.Open();
        using var ms = new MemoryStream();
        stream.CopyTo(ms);
        return ms.ToArray();
    }

    private static string ResolveImageName(Image image, MemoryImage content, string fallbackChannelName)
    {
        if (!string.IsNullOrWhiteSpace(image.Name))
            return image.Name!;

        if (!string.IsNullOrWhiteSpace(content.SourcePath))
            return Path.GetFileNameWithoutExtension(content.SourcePath);

        return fallbackChannelName;
    }
