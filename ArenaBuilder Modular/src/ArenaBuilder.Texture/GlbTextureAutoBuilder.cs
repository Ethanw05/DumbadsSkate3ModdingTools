        ResolvedGlbTextureSources sources,
        string outputDirectory,
        TextureDeduplicationRegistry deduplicationRegistry,
        bool isSmallVariant,
        int smallDownscaleFactor,
        int smallMinDim,
        LightmapEmitMode lightmapMode,
