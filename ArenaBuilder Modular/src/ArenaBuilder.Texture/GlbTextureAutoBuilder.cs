using ArenaBuilder.Core.Platforms.PS3.Pegasus.Mesh;
using ArenaBuilder.Core.Psg;
using ArenaBuilder.Texture.Dds;
using SharpGLTF.Memory;
using SharpGLTF.Schema2;
using System.Collections.Concurrent;

namespace ArenaBuilder.Texture;

/// <summary>
/// Extracts textures from a GLB material, converts PNG/JPG to DDS (DXT5), builds texture PSG files,
/// and returns channel GUIDs so mesh material overrides can link to them.
/// Supports optional BlenRose sidecar JSON material metadata while resolving textures from the GLB.
/// </summary>
public static class GlbTextureAutoBuilder
{
    private sealed record ResolvedTextureSource(
        byte[] EncodedImageBytes,
        bool SourceIsDds,
        string ImageName);

    private static readonly ConcurrentDictionary<string, object> OutputWriteLocks =
        new(StringComparer.OrdinalIgnoreCase);

    /// <summary>Clears accumulated per-path write locks after a large build (safe between builds).</summary>
    public static void ClearOutputWriteLocksCache() => OutputWriteLocks.Clear();

    /// <summary>
    /// Build-scoped memo of the Sobel-normal / linear-specular autogen, keyed by
    /// the diffuse content hash. The per-cell lightmap pipeline produces one
    /// logical source per (diffuse × lightmap-atlas), so the SAME diffuse is
    /// resolved through here dozens–hundreds of times; without this the Sobel
    /// pass re-ran for every atlas source (the "Build DLC hang"). The encode
    /// dedup downstream already collapses the output — this collapses the CPU.
    /// </summary>
    private static readonly ConcurrentDictionary<string, byte[]> AutogenCache = new();

    /// <summary>Clears the autogen memo. Safe between builds.</summary>
    public static void ClearAutogenCache() => AutogenCache.Clear();

    private static string ContentKey(string tag, byte[] bytes)
    {
        using var sha = System.Security.Cryptography.SHA1.Create();
        return tag + ":" + Convert.ToHexString(sha.ComputeHash(bytes));
    }

    public sealed record BuiltTexturePsg(
        string ChannelName,
        string SourceImageName,
        int Width,
        int Height,
        ulong Guid,
        string PsgPath);

    /// <summary>
    /// When materials JSON was used, this is the material_name (e.g. "environment.default") to use as AttributorMaterialName stream in the mesh PSG.
    /// ChannelConfig: when present, used to build only the channels defined in the JSON (textures + scalars).
    /// </summary>
    public sealed record GlbTextureAutoBuildResult(
        ulong? DiffuseGuid,
        ulong? NormalGuid,
        ulong? LightmapGuid,
        ulong? SpecularGuid,
        IReadOnlyList<BuiltTexturePsg> BuiltTextures,
        IReadOnlyList<string> Warnings,
        string? AttributorMaterialStream = null,
        RenderMaterialDataBuilder.BlenroseChannelConfig? ChannelConfig = null)
    {
        public bool HasOverrides =>
            DiffuseGuid.HasValue ||
            NormalGuid.HasValue ||
            LightmapGuid.HasValue ||
            SpecularGuid.HasValue;
    }

    /// <summary>
    /// One channel of a GLB resolved to its pre-encode source bytes plus the encoder flags
    /// <see cref="BuildTextureFromEncodedSource"/> needs. Cheap value object that holds an
    /// already-loaded byte[] and the encode-side decisions (mip count, alpha behavior) so that
    /// emitting the same GLB into N tiles only needs to re-run the encode/PSG-write step,
    /// not the GLB load + channel resolve + autogen step.
    /// </summary>
    public sealed record ResolvedChannelSource(
        string MeshChannelName,
        byte[] EncodedImageBytes,
        bool SourceIsDds,
        string ImageName,
        bool NeedsAlpha,
        bool IsLightmapChannel,
        bool GenerateMipMaps);

    /// <summary>
    /// Result of <see cref="ResolveSourcesFromGlb"/>: the GLB has been loaded, the JSON sidecar
    /// resolved, the diffuse/normal/lightmap/specular channels picked, and any autogenerated
    /// normal/specular maps synthesized from the diffuse. Pass this to <see cref="EmitToTile"/>
    /// once per cPres tile that needs a copy of these textures.
    /// </summary>
    public sealed record ResolvedGlbTextureSources(
        string GlbPath,
        string GlbIdentity,
        string MaterialName,
        ResolvedChannelSource? Diffuse,
        ResolvedChannelSource? Normal,
        ResolvedChannelSource? Lightmap,
        ResolvedChannelSource? Specular,
        bool GeneratedNormal,
        bool GeneratedSpecular,
        string? AttributorMaterialStream,
        RenderMaterialDataBuilder.BlenroseChannelConfig? ChannelConfig,
        IReadOnlyList<string> ResolveWarnings);

    /// <summary>
    /// Auto-builds texture PSGs for one material context in the GLB.
    /// Channel mapping (matches BlenRose node setup):
    /// BaseColor -> diffuse, Normal -> normal, Emissive -> lightmap (BlenRose connects lightmap to Emission),
    /// Occlusion -> lightmap fallback, MetallicRoughness -> specular.
    /// Textures are resolved from the GLB by image_name hint or by standard GLB material channels.
    /// When <paramref name="deduplicationRegistry"/> is provided, identical payloads are deduplicated
    /// (shared GUID, single PSG file); lightmaps dedupe only when payload is identical.
    ///
    /// Internally this is now a thin wrapper around <see cref="ResolveSourcesFromGlb"/> +
    /// <see cref="EmitToTile"/>; tile-build callers should call those directly to share one GLB
    /// load across many cPres tiles.
    /// </summary>
    public static GlbTextureAutoBuildResult BuildFromGlb(
        string glbPath,
        string outputDirectory,
        bool generateMipMaps = true,
        string? materialsJsonPath = null,
        string? materialNameOverride = null,
        string? guidNamespace = null,
        TextureDeduplicationRegistry? deduplicationRegistry = null,
        DerivedTextureGenerator.NormalSynthSettings? normalSynthSettings = null,
        string? deduplicationScope = null,
        CancellationToken cancellationToken = default)
    {
        var sources = ResolveSourcesFromGlb(
            glbPath,
            generateMipMaps,
            materialsJsonPath,
            materialNameOverride,
            guidNamespace,
            normalSynthSettings,
            cancellationToken);

        return EmitToTile(
            sources,
            outputDirectory,
            deduplicationRegistry,
            deduplicationScope,
            cancellationToken);
    }

    /// <summary>
    /// Phase 1 of the texture build: load the GLB, resolve the JSON sidecar, pick the four
    /// channels (diffuse / normal / lightmap / specular), and synthesize autogen normal+specular
    /// from the diffuse when those channels are missing. The expensive raster ops (PNG decode,
    /// Sobel for autogen normal, BCn encode) are NOT done here — only the cheap-ish source
    /// resolution. The returned <see cref="ResolvedGlbTextureSources"/> can be reused for
    /// multiple <see cref="EmitToTile"/> calls so that the GLB is parsed and the autogen runs
    /// only once per build per glbPath, even when the GLB feeds many cPres tiles.
    /// </summary>
    public static ResolvedGlbTextureSources ResolveSourcesFromGlb(
        string glbPath,
        bool generateMipMaps = true,
        string? materialsJsonPath = null,
        string? materialNameOverride = null,
        string? guidNamespace = null,
        DerivedTextureGenerator.NormalSynthSettings? normalSynthSettings = null,
        CancellationToken cancellationToken = default)
    {
        if (string.IsNullOrWhiteSpace(glbPath))
            throw new ArgumentException("GLB path is required.", nameof(glbPath));
        if (!File.Exists(glbPath))
            throw new FileNotFoundException("GLB file not found.", glbPath);

        var warnings = new List<string>();

        var model = ModelRoot.Load(glbPath);
        string glbStem = Path.GetFileNameWithoutExtension(glbPath);
        string glbIdentity = BuildScopedGlbIdentity(glbPath, glbStem, guidNamespace);

        if (model.LogicalMeshes.Count == 0 || model.LogicalMeshes[0].Primitives.Count == 0)
        {
            warnings.Add("GLB has no meshes/primitives; skipped texture build.");
            return new ResolvedGlbTextureSources(
                glbPath, glbIdentity, materialNameOverride ?? "DefaultMaterial",
                null, null, null, null,
                false, false,
                null, null,
                warnings);
        }

        var prim = model.LogicalMeshes[0].Primitives[0];
        var material = prim.Material ?? model.LogicalMaterials.FirstOrDefault();
        if (material == null)
        {
            warnings.Add("No material found on first primitive; skipped texture build.");
            return new ResolvedGlbTextureSources(
                glbPath, glbIdentity, materialNameOverride ?? "DefaultMaterial",
                null, null, null, null,
                false, false,
                null, null,
                warnings);
        }

        if (!string.IsNullOrWhiteSpace(materialNameOverride))
        {
            var matchedMaterial = model.LogicalMaterials.FirstOrDefault(
                m => string.Equals(m.Name, materialNameOverride, StringComparison.OrdinalIgnoreCase));
            if (matchedMaterial != null)
                material = matchedMaterial;
        }

        string materialName = !string.IsNullOrWhiteSpace(materialNameOverride)
            ? materialNameOverride!
            : (string.IsNullOrWhiteSpace(material.Name) ? "DefaultMaterial" : material.Name);

        var jsonMaterial = TryResolveJsonMaterialConfig(glbPath, materialsJsonPath, materialName, glbStem, warnings);

        // Diffuse.
        cancellationToken.ThrowIfCancellationRequested();
        var diffuseSrc = TryResolveChannelSource(material, jsonMaterial, "diffuse", ["BaseColor"], "diffuse", out string? diffWarn);
        if (diffWarn != null) warnings.Add(diffWarn);
        var diffuseChannel = diffuseSrc != null
            ? new ResolvedChannelSource(
                "diffuse",
                diffuseSrc.EncodedImageBytes,
                diffuseSrc.SourceIsDds,
                diffuseSrc.ImageName,
                NeedsAlpha: ChannelNeedsAlpha("diffuse", jsonMaterial),
                IsLightmapChannel: false,
                GenerateMipMaps: generateMipMaps)
            : null;

        // Normal: prefer GLB/JSON, fall back to autogen-from-diffuse.
        cancellationToken.ThrowIfCancellationRequested();
        var normalSrc = TryResolveChannelSource(material, jsonMaterial, "normal", ["Normal"], "normal", out string? normWarn);
        if (normWarn != null) warnings.Add(normWarn);
        ResolvedChannelSource? normalChannel = null;
        bool generatedNormal = false;
        if (normalSrc != null)
        {
            normalChannel = new ResolvedChannelSource(
                "normal",
                normalSrc.EncodedImageBytes,
                normalSrc.SourceIsDds,
                normalSrc.ImageName,
                NeedsAlpha: ChannelNeedsAlpha("normal", jsonMaterial),
                IsLightmapChannel: false,
                GenerateMipMaps: generateMipMaps);
        }
        else if (diffuseSrc != null)
        {
            try
            {
                cancellationToken.ThrowIfCancellationRequested();
                var synthSettings = normalSynthSettings ?? DerivedTextureGenerator.DefaultNormalSettings;
                byte[] normalPng = DerivedTextureGenerator.GenerateNormalMapPngFromImage(diffuseSrc.EncodedImageBytes, synthSettings);
                string generatedName = BuildGeneratedImageName(diffuseSrc.ImageName, "autogen_normal_sobel_v1");
                normalChannel = new ResolvedChannelSource(
                    "normal",
                    normalPng,
                    SourceIsDds: false,
                    ImageName: generatedName,
                    NeedsAlpha: false,
                    IsLightmapChannel: false,
                    GenerateMipMaps: generateMipMaps);
                generatedNormal = true;
            }
            catch (Exception ex)
            {
                warnings.Add($"Auto-generated normal map skipped: {ex.Message}");
            }
        }

        // Lightmap: forced single-mip (matches retail PS3 lightmap output).
        cancellationToken.ThrowIfCancellationRequested();
        var lightmapSrc = TryResolveChannelSource(material, jsonMaterial, "lightmap", ["Emissive", "Occlusion"], "lightmap", out string? lightWarn);
        if (lightWarn != null) warnings.Add(lightWarn);
        var lightmapChannel = lightmapSrc != null
            ? new ResolvedChannelSource(
                "lightmap",
                lightmapSrc.EncodedImageBytes,
                lightmapSrc.SourceIsDds,
                lightmapSrc.ImageName,
                NeedsAlpha: ChannelNeedsAlpha("lightmap", jsonMaterial),
                IsLightmapChannel: true,
                GenerateMipMaps: false)
            : null;

        // Specular: prefer GLB/JSON, fall back to autogen-from-diffuse.
        cancellationToken.ThrowIfCancellationRequested();
        var specularSrc = TryResolveChannelSource(material, jsonMaterial, "specular", ["MetallicRoughness"], "specular", out string? specWarn);
        if (specWarn != null) warnings.Add(specWarn);
        ResolvedChannelSource? specularChannel = null;
        bool generatedSpecular = false;
        if (specularSrc != null)
        {
            specularChannel = new ResolvedChannelSource(
                "specular",
                specularSrc.EncodedImageBytes,
                specularSrc.SourceIsDds,
                specularSrc.ImageName,
                NeedsAlpha: ChannelNeedsAlpha("specular", jsonMaterial),
                IsLightmapChannel: false,
                GenerateMipMaps: generateMipMaps);
        }
        else if (diffuseSrc != null)
        {
            try
            {
                cancellationToken.ThrowIfCancellationRequested();
                byte[] specularPng = DerivedTextureGenerator.GenerateSpecularMapPngFromImage(diffuseSrc.EncodedImageBytes);
                string generatedName = BuildGeneratedImageName(diffuseSrc.ImageName, "autogen_specular_linear_v1");
                specularChannel = new ResolvedChannelSource(
                    "specular",
                    specularPng,
                    SourceIsDds: false,
                    ImageName: generatedName,
                    NeedsAlpha: false,
                    IsLightmapChannel: false,
                    GenerateMipMaps: generateMipMaps);
                generatedSpecular = true;
            }
            catch (Exception ex)
            {
                warnings.Add($"Auto-generated specular map skipped: {ex.Message}");
            }
        }

        string? attributorStream = jsonMaterial != null ? jsonMaterial.MaterialName : null;
        var channelConfig = BlenroseMaterialJsonReader.ToChannelConfig(jsonMaterial);
        channelConfig = EnsureGeneratedChannelsInConfig(channelConfig, generatedNormal, generatedSpecular);

        return new ResolvedGlbTextureSources(
            glbPath,
            glbIdentity,
            materialName,
            diffuseChannel,
            normalChannel,
            lightmapChannel,
            specularChannel,
            generatedNormal,
            generatedSpecular,
            attributorStream,
            channelConfig,
            warnings);
    }

    /// <summary>
    /// In-memory sibling of <see cref="ResolveSourcesFromGlb"/>. Builds a
    /// <see cref="ResolvedGlbTextureSources"/> from raw PNG/JPG/DDS bytes per channel
    /// instead of loading them out of a GLB on disk. Generic (no GLB file I/O):
    /// callers provide whatever channels they have, and missing normal/specular maps
    /// are auto-synthesised from the diffuse exactly the same way the GLB path does.
    /// A null diffuse with non-null derived channels is permitted but unusual —
    /// autogen requires the diffuse bytes. (No surviving caller after the Godot
    /// direct-feed path was removed; retained as a generic helper.)
    /// </summary>
    /// <param name="sourceKey">Opaque per-source identifier used as the equivalent of
    ///     <c>glbPath</c> in deduplication / lookup keys. Stable across builds → stable GUIDs.</param>
    /// <param name="materialName">Material name written into the mesh PSG override and into
    ///     the texture GUID derivation. Required.</param>
    /// <param name="diffusePng">PNG/JPG/DDS bytes for the BaseColor channel, or null.</param>
    /// <param name="normalPng">Explicit normal-map bytes, or null to autogen from diffuse.</param>
    /// <param name="lightmapPng">Lightmap bytes, or null. No fallback — engines that need a
    ///     lightmap and get none use their internal default texture.</param>
    /// <param name="specularPng">Explicit specular bytes, or null to autogen from diffuse.</param>
    public static ResolvedGlbTextureSources ResolveSourcesFromBytes(
        string sourceKey,
        string materialName,
        byte[]? diffusePng,
        byte[]? normalPng = null,
        byte[]? lightmapPng = null,
        byte[]? specularPng = null,
        bool generateMipMaps = true,
        string? guidNamespace = null,
        DerivedTextureGenerator.NormalSynthSettings? normalSynthSettings = null,
        string? attributorMaterialName = null,
        CancellationToken cancellationToken = default)
    {
        if (string.IsNullOrWhiteSpace(sourceKey))
            throw new ArgumentException("sourceKey is required.", nameof(sourceKey));
        if (string.IsNullOrWhiteSpace(materialName))
            throw new ArgumentException("materialName is required.", nameof(materialName));

        var warnings = new List<string>();
        string identity = BuildScopedGlbIdentity(sourceKey, sourceKey, guidNamespace);

        // A transparent/alpha AttributorMaterial (e.g. environment.transparent)
        // means the engine's shader samples the diffuse alpha — so the diffuse
        // PNG must encode as DXT5 (alpha-preserving), not DXT1. Without this the
        // alpha never ships and the mesh renders fully opaque no matter which
        // material class it was tagged with.
        bool diffuseNeedsAlpha = AttributorWantsDiffuseAlpha(attributorMaterialName);

        // Diffuse — used both directly and as the source for autogen normal/specular.
        ResolvedChannelSource? diffuseChannel = null;
        if (diffusePng != null && diffusePng.Length > 0)
        {
            diffuseChannel = new ResolvedChannelSource(
                "diffuse",
                diffusePng,
                SourceIsDds: false,
                ImageName: $"{materialName}_diffuse",
                NeedsAlpha: diffuseNeedsAlpha,
                IsLightmapChannel: false,
                GenerateMipMaps: generateMipMaps);
        }

        // Normal — explicit, else autogen, else skip.
        cancellationToken.ThrowIfCancellationRequested();
        ResolvedChannelSource? normalChannel = null;
        bool generatedNormal = false;
        if (normalPng != null && normalPng.Length > 0)
        {
            normalChannel = new ResolvedChannelSource(
                "normal",
                normalPng,
                SourceIsDds: false,
                ImageName: $"{materialName}_normal",
                NeedsAlpha: false,
                IsLightmapChannel: false,
                GenerateMipMaps: generateMipMaps);
        }
        else if (diffuseChannel != null)
        {
            try
            {
                cancellationToken.ThrowIfCancellationRequested();
                var synthSettings = normalSynthSettings ?? DerivedTextureGenerator.DefaultNormalSettings;
                // Memoized by diffuse content hash. A build uses one settings
                // instance for every source, so the hash need not fold it in.
                byte[] genPng = AutogenCache.GetOrAdd(
                    ContentKey("nrm", diffuseChannel.EncodedImageBytes),
                    _ => DerivedTextureGenerator.GenerateNormalMapPngFromImage(diffuseChannel.EncodedImageBytes, synthSettings));
                string generatedName = BuildGeneratedImageName(diffuseChannel.ImageName, "autogen_normal_sobel_v1");
                normalChannel = new ResolvedChannelSource(
                    "normal",
                    genPng,
                    SourceIsDds: false,
                    ImageName: generatedName,
                    NeedsAlpha: false,
                    IsLightmapChannel: false,
                    GenerateMipMaps: generateMipMaps);
                generatedNormal = true;
            }
            catch (Exception ex)
            {
                warnings.Add($"Auto-generated normal map skipped: {ex.Message}");
            }
        }

        // Lightmap — no autogen. Forced single-mip to match retail PS3 lightmap output.
        cancellationToken.ThrowIfCancellationRequested();
        ResolvedChannelSource? lightmapChannel = null;
        if (lightmapPng != null && lightmapPng.Length > 0)
        {
            lightmapChannel = new ResolvedChannelSource(
                "lightmap",
                lightmapPng,
                SourceIsDds: false,
                ImageName: $"{materialName}_lightmap",
                NeedsAlpha: false,
                IsLightmapChannel: true,
                GenerateMipMaps: false);
        }

        // Specular — explicit, else autogen from diffuse, else skip.
        cancellationToken.ThrowIfCancellationRequested();
        ResolvedChannelSource? specularChannel = null;
        bool generatedSpecular = false;
        if (specularPng != null && specularPng.Length > 0)
        {
            specularChannel = new ResolvedChannelSource(
                "specular",
                specularPng,
                SourceIsDds: false,
                ImageName: $"{materialName}_specular",
                NeedsAlpha: false,
                IsLightmapChannel: false,
                GenerateMipMaps: generateMipMaps);
        }
        else if (diffuseChannel != null)
        {
            try
            {
                cancellationToken.ThrowIfCancellationRequested();
                byte[] genPng = AutogenCache.GetOrAdd(
                    ContentKey("spec", diffuseChannel.EncodedImageBytes),
                    _ => DerivedTextureGenerator.GenerateSpecularMapPngFromImage(diffuseChannel.EncodedImageBytes));
                string generatedName = BuildGeneratedImageName(diffuseChannel.ImageName, "autogen_specular_linear_v1");
                specularChannel = new ResolvedChannelSource(
                    "specular",
                    genPng,
                    SourceIsDds: false,
                    ImageName: generatedName,
                    NeedsAlpha: false,
                    IsLightmapChannel: false,
                    GenerateMipMaps: generateMipMaps);
                generatedSpecular = true;
            }
            catch (Exception ex)
            {
                warnings.Add($"Auto-generated specular map skipped: {ex.Message}");
            }
        }

        return new ResolvedGlbTextureSources(
            sourceKey,
            identity,
            materialName,
            diffuseChannel,
            normalChannel,
            lightmapChannel,
            specularChannel,
            generatedNormal,
            generatedSpecular,
            AttributorMaterialStream: string.IsNullOrWhiteSpace(attributorMaterialName)
                ? null
                : attributorMaterialName,
            ChannelConfig: null,
            ResolveWarnings: warnings);
    }

    /// <summary>
    /// True when an AttributorMaterialName's subclass samples the diffuse alpha
    /// channel (transparent / alpha / alphatest families). Mirrors BlenRose's
    /// alpha-aware subclasses; drives DXT5-vs-DXT1 selection for the diffuse.
    /// </summary>
    private static bool AttributorWantsDiffuseAlpha(string? attributorMaterialName)
    {
        if (string.IsNullOrWhiteSpace(attributorMaterialName))
            return false;
        string name = attributorMaterialName!;
        int dot = name.IndexOf('.');
        string sub = dot >= 0 ? name[(dot + 1)..] : name;
        return sub.Equals("transparent", StringComparison.OrdinalIgnoreCase)
            || sub.Equals("alpha", StringComparison.OrdinalIgnoreCase)
            || sub.Equals("alphatest", StringComparison.OrdinalIgnoreCase)
            || sub.Equals("reflective_trans", StringComparison.OrdinalIgnoreCase)
            || sub.Equals("flowingalpha", StringComparison.OrdinalIgnoreCase)
            || sub.Equals("skatepark_alpha", StringComparison.OrdinalIgnoreCase);
    }

    /// <summary>
    /// Computes the deterministic FULL-variant GUID for a mesh-bound channel: the GUID has
    /// <see cref="TextureGuidStrategy.SmallVariantFlag"/> (bit 62) cleared so it is safe to use
    /// as a mesh-channel reference. The engine resolves the full copy from cTex via this GUID,
    /// or — on a primary lookup miss — sets bit 62 and resolves the small copy from cPres.
    /// </summary>
    public static ulong ComputeChannelFullGuid(
        ResolvedGlbTextureSources sources,
        ResolvedChannelSource channel)
    {
        if (sources is null) throw new ArgumentNullException(nameof(sources));
        if (channel is null) throw new ArgumentNullException(nameof(channel));
        string textureKey = TextureGuidStrategy.BuildTextureKey(
            sources.GlbIdentity,
            sources.MaterialName,
            channel.MeshChannelName,
            channel.ImageName);
        return TextureGuidStrategy.KeyToGuid(textureKey);
    }

    /// <summary>
    /// Default linear downscale factor applied to the small (cPres) fallback variant.
    /// Empirically the stock <c>DLC_DW_MegaCompund</c> distribution pairs every full↔small
    /// sibling as exactly 8× linear (= 64× pixels), e.g. 512×512 ↔ 64×64, 256×128 ↔ 32×16.
    /// </summary>
    public const int DefaultSmallVariantDownscaleFactor = 8;

    /// <summary>
    /// Lower bound on the small variant's longest side, regardless of source dimensions.
    /// 8 px keeps DXT 4×4-block encoding valid for both axes.
    /// </summary>
    public const int DefaultSmallVariantMinDim = 8;

    /// <summary>
    /// Controls how the lightmap channel is routed relative to the dual-tier (small cPres +
    /// full cTex) scheme used by every other channel.
    /// </summary>
    /// <remarks>
    /// Skate 3's engine loads lightmap PSGs out of the per-tile <c>cPres_&lt;X&gt;_&lt;Y&gt;_high</c>
    /// collection (resident with that tile's geometry), NOT the streaming
    /// <c>cTex_&lt;X&gt;_&lt;Y&gt;_high</c> collection. So lightmaps must be full-resolution and
    /// must live in cPres, never cTex — the opposite of the diffuse/normal/specular dual-tier
    /// policy.
    /// </remarks>
    /// <summary>Per-channel emit decision returned by a
    /// <see cref="ChannelPlacementResolver"/>. Lets the budget planner in
    /// <c>ArenaBuilder.Build</c> drive placement without this Texture-layer
    /// type referencing any Glb tile-grid types.</summary>
    public enum ChannelEmitDecision
    {
        /// <summary>Don't write this channel into this tile at all (promoted
        /// elsewhere, or cTex-budget-rejected).</summary>
        Skip,
        /// <summary>Write the SMALL (mip-sliced) fallback under the
        /// bit-62-set sibling GUID.</summary>
        Small,
        /// <summary>Write the FULL-resolution copy under the full GUID.</summary>
        Full,
    }

    /// <summary>Resolver the pipeline supplies so the budget plan (which
    /// knows tile geometry) decides each channel's fate for the tile being
    /// emitted. <c>fullGuid</c> is the channel's full-variant GUID;
    /// <c>isLightmapChannel</c> lets the resolver keep the lightmap-specific
    /// rules. Return <see cref="ChannelEmitDecision"/>.</summary>
    public delegate ChannelEmitDecision ChannelPlacementResolver(ulong fullGuid, bool isLightmapChannel);

    public enum LightmapEmitMode
    {
        /// <summary>
        /// Lightmap channel follows the same variant policy as every other channel
        /// (full-res in a full-variant emit, 1/8 small in a small-variant emit). This is the
        /// legacy behaviour; correct for GlobalOnly's single self-contained collection.
        /// </summary>
        Default,

        /// <summary>
        /// Skip the lightmap channel entirely for this emit. Used when writing a streaming
        /// <c>cTex_*</c> tile: lightmaps must not appear in cTex.
        /// </summary>
        SkipLightmap,

        /// <summary>
        /// Emit the lightmap channel at FULL resolution under the FULL GUID (bit 62 clear),
        /// even though the rest of this emit is a small (cPres) variant. The mesh material ref
        /// points at the FULL GUID; with no cTex copy in existence the engine's primary lookup
        /// must resolve directly out of this cPres tile, so the cPres copy has to be the
        /// full-res, full-GUID one.
        /// </summary>
        CPresFullResLightmap,
    }

    /// <summary>
    /// Phase 2 of the texture build — emits one Texture PSG per channel of <paramref name="sources"/>
    /// into <paramref name="outputDirectory"/> under the FULL-variant GUID
    /// (<see cref="ComputeChannelFullGuid"/>; bit 62 cleared). Use this for cTex tiles AND for
    /// GlobalOnly's single self-contained collection — anywhere the texture lives at full resolution.
    /// </summary>
    /// <param name="lightmapMode">
    /// For cTex tiles pass <see cref="LightmapEmitMode.SkipLightmap"/> (lightmaps never go to
    /// cTex). For GlobalOnly's self-contained collection leave it
    /// <see cref="LightmapEmitMode.Default"/> (the whole map is resident, so the lightmap can
    /// live next to everything else at full res).
    /// </param>
    /// <remarks>
    /// The encode-once cache means BCn runs at most ONE time per (sourceBytes, encode flags,
    /// maxDim) tuple across the whole build, regardless of how many cTex tiles end up calling this.
